package com.example.questbasickotlin_0167

fun main() {

    // Byte
    val byteValue: Byte = 10
    println("Byte: $byteValue")

    // int
    val intValue: Int =30
    println(intValue)

    //Float
    val floatValue: Float = 50.6f //kalau float pake 'f' Kalau double tidak pakai 'f'
    println("Float: $floatValue")

    //Double
    val doubleValue: Double = 10.5
    println("Double: $doubleValue")

    //char
    val charValue: Char = 'A'
    println("Char: $charValue")

    //boolean
    val booleanValue: Boolean = true
    println("Boolean : $booleanValue")

    //string
    var stringValue: String = "Hello, World!"
    println("String: $stringValue")

    stringValue = "Hello, Kotlin!"
    println("String:stringValue")


}